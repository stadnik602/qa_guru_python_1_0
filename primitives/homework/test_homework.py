# Сделайте функцию, которая будет печатать
# читаемое имя переданной ей функции и значений аргументов.
# Вызовите ее внутри функций, описанных ниже
# Подсказка: Имя функции можно получить с помощью func.__name__
# Например, вызов следующей функции должен преобразовать имя функции
# в более читаемый вариант (заменить символ подчеркивания на пробел,
# сделать буквы заглавными (или первую букву), затем вывести значения всех аргументов этой функции:
# >>> open_browser(browser_name="Chrome")
# "Open Browser [Chrome]"

def readable_name(func, **kwargs):
    func_name = func.__name__.replace('_', ' ').title()
    args_name = ", ".join(str(value) for value in kwargs.values())
    return f"{func_name} [{args_name}]"

def test_readable_function():
    open_browser(browser_name="Chrome")
    go_to_companyname_homepage(page_url="https://companyname.com")
    find_registration_button_on_login_page(page_url="https://companyname.com/login", button_text="Register")
    find_registration_button_on_login_page_2(page_url="https://companyname.com/login", button_text="Register")


def open_browser(browser_name):
    actual_result = readable_name(open_browser, browser_name=browser_name)
    assert actual_result == "Open Browser [Chrome]"


def go_to_companyname_homepage(page_url):
    actual_result = readable_name(go_to_companyname_homepage, page_url=page_url)
    assert actual_result == "Go To Companyname Homepage [https://companyname.com]"


def find_registration_button_on_login_page(page_url, button_text):
    actual_result = readable_name(find_registration_button_on_login_page, page_url=page_url, button_text=button_text)
    assert actual_result == "Find Registration Button On Login Page [https://companyname.com/login, Register]"



def even_more_readable_name(func, **kwargs):
    func_name = func.__name__.replace('_', ' ').title()
    args_name = ", ".join(key + "=" + kwargs[key] for key in kwargs.keys())
    return f"{func_name} [{args_name}]"

def find_registration_button_on_login_page_2(page_url, button_text):
    actual_result = even_more_readable_name(find_registration_button_on_login_page_2, page_url=page_url, button_text=button_text)
    assert actual_result == "Find Registration Button On Login Page 2 [page_url=https://companyname.com/login, button_text=Register]"


